--CREAR TABLAS

--------PERSONAS-----
CREATE TABLE PERSONA (
    NOMBRE VARCHAR2(20),
    APELLIDOS VARCHAR2(40),
    PROFESION VARCHAR2(20),
    CI INTEGER CHECK (CI BETWEEN 150 AND 300),
    EMAIL VARCHAR2(30) UNIQUE CHECK (EMAIL LIKE '%@%'),
    ID_PROYECTO NUMBER(4),
    CONSTRAINT PK_PERSONA PRIMARY KEY (NOMBRE, APELLIDOS),
    CONSTRAINT FK_PERSONA_PROYECTO FOREIGN KEY (ID_PROYECTO)
        REFERENCES PROYECTO(ID_PROYECTO),
    CONSTRAINT CK_PROFESION CHECK (PROFESION IN ('INVESTIGADOR','ADJUNTO','AUXILIAR','ESTUDIANTE'))
);


-----PROYECTO-----

CREATE TABLE PROYECTO (
    ID_PROYECTO NUMBER(4) PRIMARY KEY,
    NOMBRE_PROYECTO VARCHAR2(20) NOT NULL UNIQUE,
    PRESUPUESTO NUMBER(5,2),
    DIRECTOR_NOMBRE VARCHAR2(20),
    DIRECTOR_APELLIDOS VARCHAR2(40)
);

---checkpoint---

CREATE TABLE CHECKPOINT (
    ID_PROYECTO NUMBER(4),
    ID_HOBBY NUMBER(2),
    FECHA DATE DEFAULT SYSDATE,
    PROGRESO INTEGER CHECK (PROGRESO BETWEEN 5 AND 100),
    CONSTRAINT PK_CHECKPOINT PRIMARY KEY (ID_PROYECTO, ID_HOBBY),
    CONSTRAINT FK_CHECKPOINT_PROYECTO FOREIGN KEY (ID_PROYECTO)
        REFERENCES PROYECTO(ID_PROYECTO)
);

